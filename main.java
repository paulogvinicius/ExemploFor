public class ExemploFor {
    public static void main(String[] args) {
        
        // O loop começa em 1, vai até 5, e aumenta de 1 em 1 a cada rodada
        for (int i = 1; i <= 5; i++) {
            System.out.println("Contagem: " + i);
        }
        
        System.out.println("Fim do loop!");
    }
}
